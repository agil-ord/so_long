char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	char	*str;
	size_t	len;

	len = ft_strlen(s);
	i = 0;
	str = (char *) ft_calloc(len + 1, sizeof(*s));
	if (!str)
		return (NULL);
	while (i < len)
	{
		str[i] = f((unsigned int)i, s[i]);
		i++;
	}
	return (str);
}
