#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	count;

	if (!lst)
		return (0);
	count = 0;
	while (lst -> next != 0)
	{
		lst = lst -> next;
		count++;
	}
	if (lst -> next == 0)
		count++;
	return (count);
}
